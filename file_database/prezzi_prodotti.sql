CREATE TABLE supermarkets_products AS
SELECT 
    s.name AS supermarket_name, 
    s.chain AS supermarket_chain, 
    p.product_name AS product_name, 
    ROUND(p.price + (RAND() - 0.5) * 0.5, 2) AS price
FROM 
    supermarkets s 
CROSS JOIN 
    products_prices p;
